<h1 class="entry-title">GSB-Application de gestion des frais</h1>

<p><strong>Contexte GSB</strong> : Le laboratoire Galaxy-Swiss-Bourdin souhaite mettre en place une application web de gestion des frais à disposition de ses visiteurs médicaux.<br />
L’application doit permettre d’authentifier deux types d’utilisateurs (visiteurs/comptable), d’ajouter et d’enregistrer tout frais engagé, aussi bien pour l’activité directe (déplacement, restauration et hébergement) que pour les activités annexes (événementiel, conférences, autres), et de présenter un suivi daté des opérations menées par le service comptable (réception des pièces, validation de la demande de remboursement, mise en paiement, remboursement effectué).</p>

<p><strong>Mission 1:</strong> Développement d’une application web dissociant deux parties (visiteurs/comptables). les visiteurs médicaux peuvent saisir leurs frais de déplacements, les comptable peuvent en suivant réaliser leur campagne de validation mensuelle des frais saisis au préalable par les visiteurs médicaux.</p>

<p><strong>Développement du projet : </strong>La réalisation de cette application à été fait en groupe de 4 à l&rsquo;aide des langages HTML, PHP, CSS et SQL.<br />
