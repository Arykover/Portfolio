<h1 class="entry-title">TP GLPI</h1>

<p><strong>Contexte :</strong> Ce Tp consistait en la mise en place d'un serveur virtuel windows Server 2008 et de sa configuration afin d'y installer GLPI pour simuler l'envoi d'un ticket d'assistance et sa réponse par un technicien</p>

<p><strong>Objectif :</strong> Développement d’une application web en groupe, .</p>

    <strong>Compétences mises en œuvre </strong>:
    - Mise en place et configuration d'un serveur virtuel, installation apache, mysql et GLPI </p>
<p>-  Simulation d'incident : envoi de ticket d'incident et reception par le technicien </p>
