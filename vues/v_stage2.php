<p><strong>Contexte :</strong> L'intendance du lycée Voillaume a presenté le besoin d'une application web lui permettant l'enrengistrement et la completion des fiches de sécurité sociale des étudiants depuis internet. </br> La tache va de la conception totale du site et sa base de donnée jusqu'a sa mise en ligne et le choix de la solution de mise en place. </br> Tout ceci suivant un cahier des charges strict. </p>
</br>
<p><strong>Experience:</strong> </br>
    Au cours de ce stage j'ai été amené à gerer en binome la gestion totale d'un projet d'application pour un client, de sa conception a sa mise en place depuis un cahier des charges. 
</p>
</br>
<p><strong>Activités:</strong> </br>
    
    - Analyse : prise de connaissance du cahier des charges et choix de solutions</p>

<p>-  Conception : Conception d'une base de donnée, de la documentation et maquettage et prototypage de l'application.</p>

<p>-  Devellopement : devellopement de l'application </p>

<p>-  Mise en place : Choix de la solution d'hebergement, et mise en place de l'application en ligne. </p>

